					<p>&nbsp;</p>

					<p>Here's an example: I used to tell one of my clients, Alan, when I was taking the afternoon off. Without fail, he'd call me with an “emergency.” That's not just a scheduling problem—it's a boundary problem.</p>

					<p>Burnout happens when I don't take breaks, when I work too long without moving my body, when I stop seeing friends, when I stop cooking my meals and walking my dog. If I ignore my own needs for too long, I burn out.</p>

					<p>So I keep things pretty structured. I keep my home tidy. I go to the grocery store. I cook my meals. I rarely order food. I take care of myself and my space—because that's part of staying well enough to do this work.</p>

					<h2>Did you think it was important to niche down as a freelancer?</h2>

					<p>Short answer: nope. 100% stay general.</p>

					<p>If I'm reading between the lines, you might be asking if niching down gives you some kind of competitive advantage. Maybe. But I don't think so. And more importantly—it sounds boring.</p>

					<p>What I do think is important is diversifying your freelance work. Here's what that looked like for me:</p>

					<ul>
						<li>I worked directly with small businesses and handled everything start to finish. Those were most of my projects.</li>
						<li>I partnered (not legally) with an SEO specialist. He handled the client relationship and I handled the development. Sometimes communication was a challenge. That's when I learned to create checklists for client sign-off.</li>
						<li>I collaborated with a content writer. We formed an LLC (which I'll talk about in a minute), but it didn't last. We billed separately. I had client access, which was great, but there was no structure—and one of those projects dragged on for over a year. Lesson learned.</li>
						<li>I did contract work for an agency. I didn't have direct contact with the client, and I had to follow their project management system, which made it really hard to prevent endless revisions.</li>
					</ul>

					<p>So no—I don't think you need a niche. I think you need options. The broader your experience, the more flexible and valuable you become.</p>

					<h2>How did you decide if a client was a good long-term fit?</h2>

					<p>Simple:<br><strong>Do they pay my invoices, and are they nice to work with?</strong><br>If the answer is yes, they're a good long-term fit.</p>

					<h2>How did you deal with clients who constantly requested last-minute changes?</h2>

					<p>Ah, the dreaded scope creep.</p>

					<p>This is where your documentation saves you. If you don't have a clear scope of work (SOW), you're already at a disadvantage. You need a checklist for client review and sign-off. Once they sign off and the site launches, that's the end of the project.</p>

					<p>If they ask for more work, create a new scope.</p>

					<p>Yes, it's uncomfortable. Yes, it can be an awkward conversation. But clients can't respect boundaries they don't know exist. You have to talk about this early and often.</p>

					<p>And if a client is withholding payment until you make last-minute changes? Sometimes you'll have to eat it. But after that, schedule a meeting. Create a hard stop.</p>

					<p>When the project is launched and paid for, you are not obligated to keep working. You don't owe anyone free labor, especially not when the agreement is done.</p>

					<h2>What was your biggest mistake as a freelancer?</h2>

					<p>Oh, I've got two.</p>

					<p><strong>1. Forming an LLC with a collaborator.</strong><br>
						That was a painful lesson. Partnerships work best for me when they're project-based and informal. That situation was confusing, and it ended a friendship. I won't make that mistake again.</p>

					<p><strong>2. Not charging enough.</strong><br>
						When you undercharge, it's hard to save money. I took on jobs I didn't want because I was broke. There's nothing romantic about the “starving freelancer” thing. Charge enough to give yourself breathing room.</p>

