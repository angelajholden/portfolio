					<p>I got some really thoughtful questions from Lina after my last freelancing video, so I'm answering them here in blog form for anyone who prefers to read. These are things I've learned the hard way over the past decade of freelancing—through trial, error, and occasionally, total burnout. Let's dive in.</p>

					<h2>How did you manage multiple projects at once without burning out?</h2>

					<p>This is a multi-part answer, because managing multiple projects is one thing—<em>not burning out</em> is something else entirely.</p>

					<p>First, the logistics: I keep a calendar and I work on one task at a time. Every project goes through phases: proposal, scope of work, build, review, launch. Each phase has tasks, and I track those on a project board. That helps me know exactly where I am in a project without having to carry it all in my head.</p>

					<p>I can work on multiple projects in a single day, but only because I know what's done, what's next, and when my deadlines are. That comes from experience—and from being honest with myself about how long things actually take.</p>

					<p>But burnout? I don't think burnout comes from working on too many projects. I think it comes from a lack of balance and poor boundaries.</p>

					<figure>
						<img loading="lazy" src="<?php echo BASE_URL; ?>images/articles/tyler-delgado-A1kXxn2KVCM-unsplash.jpg" alt="A glass of wine" />
