					<p>As soon as I started to learn HTML and CSS, I started freelancing. Thank god I was naive—because if I'd known what I was getting into, I probably wouldn't have tried at all.</p>
					<p>It's the hardest thing I've ever done, and that includes actually writing code. From actually finding clients — to understanding what they need — to getting paid. It is either exhilarating or it will make you want to sit in the corner and cry.</p>
					<p>This isn't an exhaustive guide. It's meant to be a starting point. I'll be expanding on these topics in future videos, so drop your questions in the comments if there's something you want me to dive deeper into.</p>

					<h2>How to Find Clients</h2>
					<p>I know the first thing you're wondering is: How do I find clients?</p>
					<p>Well, I'm here to tell you that prospective clients are everywhere. Here's how I met my current client list:</p>

					<p><strong>The dog park!</strong> Literally, I was just BS'ing with people at the dog park, and when someone asked me what I do for a living, I said freelance web designer, and that's how it started. It went from a conversation about how much a new website costs, to actually being hired to build a custom website. That was eight years ago and I still manage that site.</p>

					<p>Another client heard me on a podcast about Etsy, because at one point I was selling graphic design on Etsy. But I was also a freelance web designer, and her business needed a new website. But here's the thing, she heard me on that podcast more than a year after it was originally published, and now she's been my client for nine years.</p>

					<p>Another of my clients had a WordPress website that was so badly compromised attackers had login credentials and were using his blog to post vile ads using his domain credibility. At that time I was a WordPress developer and I was able to rebuild the site and restore his SEO ranking in search results.</p>

					<p>And lastly, I can't even count the number of clients that I've gotten from running ads on Craigslist.</p>

					<p>Yes, Craigslist. And listen — the bar is on the floor on Craigslist. Sometimes I go on Craigslist to look for something, and the things people post are so poorly written I wonder why they even bothered posting at all. So when I include screenshots of my work in the image gallery and write ad copy using complete sentences, those ads get noticed.</p>

					<p>Now, when I started freelancing in 2014, sites like UpWork and Fiverr were just starting to get popular. I tried to win jobs on UpWork and was always outbid by developers who could work for much less than I could.</p>

					<p>So if you can build a freelancing business that way, congratulations — because I thought it was really hard. What ended up working best for me was placing ads on my local Craigslist, and building relationships in my community. And pretty soon I didn't need to run ads at all because I had enough referrals and repeat business that I stayed really busy.</p>

					<p>The point here is this: finding clients requires creativity and a willingness to talk to people, wherever you find yourself.</p>

